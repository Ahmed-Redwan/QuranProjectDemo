package com.example.quranprojectdemo.models.otherModels;

public class Sora {
//
    private String name_sora;
    private int number_auah;

    public Sora(String name_sora, int number_auah) {
        this.name_sora = name_sora;
        this.number_auah = number_auah;
    }

    public String getName_sora() {
        return name_sora;
    }

    public void setName_sora(String name_sora) {
        this.name_sora = name_sora;
    }

    public int getNumber_auah() {
        return number_auah;
    }

    public void setNumber_auah(int number_auah) {
        this.number_auah = number_auah;
    }
}
